        <p>El bachillerato técnico es una oferta enfocada en las y los jóvenes, para fortalecer su incorporación al mundo laboral y/o dar continuidad a su formación técnica y tecnológica de educación superior, en estrecha vinculación con el sector productivo y prioridades nacionales. Su diseño curricular se basa en el desarrollo de competencias laborales que brindan características de empleabilidad a nuestros estudiantes, en concordancia con la demanda laboral forjada según las necesidades de la matriz productiva, los sectores priorizados y las agendas zonales de desarrollo.</p>

