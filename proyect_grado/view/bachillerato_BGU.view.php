        <h1 class="T-educacion_basica centrar"><b>Bachillerato BGU</b> <br> <span><i class="fas fa-user-graduate"></i> <i class="fas fa-book-open"></i></span></h1>
        <p>El Bachillerato General Unificado es un programa de estudios creado por el Ministerio de Educación (MinEduc) con el propósito de ofrecer un mejor servicio educativo para todos los jóvenes que hayan aprobado la Educación General Básica (EGB). <br><br>

            El BGU, que consta de primero, segundo y tercer curso; tiene como triple objetivo preparar a los estudiantes: (a) para la vida y la participación en una sociedad democrática, (b) para el mundo laboral o del emprendimiento, y (c) para continuar con sus estudios universitarios.<br><br>

            Todos los estudiantes, en el BGU, deben estudiar un grupo de asignaturas centrales denominado tronco común, que les permite adquirir ciertos aprendizajes básicos correspondientes a su formación general. Además del tronco común, los estudiantes pueden escoger entre dos opciones en función de sus intereses: el Bachillerato en Ciencias o el Bachillerato Técnico.<br><br>

            Aquellos que opten por el Bachillerato en Ciencias, además de adquirir los aprendizajes básicos comunes del BGU, podrán acceder a asignaturas optativas que les permitirán profundizar en ciertas áreas académicas de su interés.<br><br>

            Los que opten por el Bachillerato Técnico también adquirirán los aprendizajes básicos comunes del BGU, y además desarrollarán las competencias específicas de la figura profesional que hayan elegido.</p>

