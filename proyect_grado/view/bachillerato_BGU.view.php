        <h2>¿Por qué un Bachillerato unificado y no uno por especializaciones?</h2>
        <p>Porque es la única manera de garantizar equidad a todos los bachilleres ecuatorianos y a la vez multiplicar sus opciones postgraduación. La base común de conocimientos y destrezas que adquirirán todos los bachilleres, independientemente del tipo de Bachillerato que elijan, les habilitará por igual para continuar estudios superiores en cualquier área académica, o ingresar directamente al mundo laboral o del emprendimiento.</p>

        <h2>¿Cuáles son las áreas de conocimiento en el BGU?</h2>
        <p>
            El currículo nacional al igual que en el nivel de Educación General Básica, el nivel de Bachillerato General Unificado está organizado por áreas de conocimiento, por lo tanto los estudiantes, para avanzar hacia el perfil de salida, deben desarrollar aprendizajes de las siguientes áreas: Lengua y Literatura, Matemática, Ciencias Naturales, Ciencias Sociales, Lengua Extranjera, Educación Física y Educación Cultural y Artística.
        </p>

